    console.log('Characters Loading:', charactersLoading);
    console.log('Characters Result:', charactersResult);
    console.log('Characters Error:', charactersError);

    const goToLocationDetail = (id) => {
      router.push(`/location/${id}`);
    };

    const goToCharacterDetail = (id) => {
      router.push(`/character/${id}`);
    };

    return {
      locationsResult,
      locationsLoading,
      locationsError,
      charactersResult,
      charactersLoading,
      charactersError,
      goToCharacterDetail,
      goToLocationDetail,
    };
  },
